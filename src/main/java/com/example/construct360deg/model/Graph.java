package com.example.construct360deg.model;

public class Graph {
    private int projectid;
    private float graphpoint;
    private String graphattribute;

    public int getProjectid() {
        return projectid;
    }

    public void setProjectid(int projectid) {
        this.projectid = projectid;
    }

    public float getGraphpoint() {
        return graphpoint;
    }

    public void setGraphpoint(float graphpoint) {
        this.graphpoint = graphpoint;
    }

    public String getGraphattribute() {
        return graphattribute;
    }

    public void setGraphattribute(String graphattribute) {
        this.graphattribute = graphattribute;
    }
}
